package dados;


public class Matricula {

    private String Matricula;
    private double Valor;
    private String Modalidade;
    private String Aluno;
    private double Inicio;
    private double dataVc;
    private String Fpagamento;
    private String CondPagamento;

    public Matricula() {
    }

    public String getMatricula() {
        return Matricula;
    }

    public void setMatricula(String Matricula) {
        this.Matricula = Matricula;
    }


    public double getValor() {
        return Valor;
    }

    public void setValor(double Valor) {
        this.Valor = Valor;
    }

    public String getModalidade() {
        return Modalidade;
    }

    public void setModalidade(String Modalidade) {
        this.Modalidade = Modalidade;
    }

    public String getAluno() {
        return Aluno;
    }

    public void setAluno(String Aluno) {
        this.Aluno = Aluno;
    }

    public double getInicio() {
        return Inicio;
    }

    public void setInicio(double Inicio) {
        this.Inicio = Inicio;
    }

    public double getDataVc() {
        return dataVc;
    }

    public void setDataVc(double dataVc) {
        this.dataVc = dataVc;
    }

    public String getFpagamento() {
        return Fpagamento;
    }

    public void setFpagamento(String Fpagamento) {
        this.Fpagamento = Fpagamento;
    }

    public String getCondPagamento() {
        return CondPagamento;
    }

    public void setCondPagamento(String CondPagamento) {
        this.CondPagamento = CondPagamento;
    }
    
    
    
    
}
